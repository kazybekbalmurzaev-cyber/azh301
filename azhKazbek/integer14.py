# Дано трехзначное число. В нем зачеркнули первую справа цифру и приписали ее слева.
# Вывести полученное число.

number = int(input("Введите трехзначное число: "))

# Получаем первую справа цифру (единицы)
last_digit = number % 10
# Получаем оставшиеся две цифры
first_two_digits = number // 10

# Формируем новое число: приписываем последнюю цифру слева
new_number = int(str(last_digit) + str(first_two_digits))

print("Полученное число:", new_number)